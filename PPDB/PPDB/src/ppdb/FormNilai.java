    private void tableNilaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNilaiMouseClicked
        // TODO add your handling code here:
        int baris = tableNilai.getSelectedRow();
        String nik = tableNilai.getValueAt(baris, 0).toString();
        if(!nik.isEmpty()){
            cNik.setSelectedItem(nik);
            tBind.setText(tableNilai.getValueAt(baris, 1).toString());
            tBingg.setText(tableNilai.getValueAt(baris, 2).toString());
            tIpa.setText(tableNilai.getValueAt(baris, 3).toString());
            tMtk.setText(tableNilai.getValueAt(baris, 4).toString());
            tNim.setText(tableNilai.getValueAt(baris, 5).toString());
            bUbah.setEnabled(true);
            bHapus.setEnabled(true);
            bSimpan.setEnabled(false);
    }                                      
    }//GEN-LAST:event_tableNilaiMouseClicked

    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
               if(tNim.getText().isEmpty()||cNik.getSelectedItem().toString().isEmpty()){   
        JOptionPane.showMessageDialog(null,"Inputan Data Anda Tidak Boleh Kosong!!!");
        }else{
            try{
                pst = conn.prepareStatement("insert into nilai values(?,?,?,?,?,?)");
                pst.setString(1, cNik.getSelectedItem().toString());
                pst.setString(2, tBind.getText());
                pst.setString(3, tBingg.getText());
                pst.setString(4, tIpa.getText());
                pst.setString(5, tMtk.getText());
                pst.setString(6, tNim.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data Nilai Berhasil Telah Disimpan!");
                ulang();
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Data Nilai Gagal Ditambahkan!!!" + ex.toString());
                Logger.getLogger(FormPendaftaran.class.getName()).log(Level.SEVERE,null,ex);
            }
        }
               tampil_cb();
    }//GEN-LAST:event_bSimpanActionPerformed

    private void bHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHitungActionPerformed
        // TODO add your handling code here:
               float bhs_ind,bhs_ingg,ipa,mtk,nim;
        
        bhs_ind = Float.parseFloat(tBind.getText());
        bhs_ingg = Float.parseFloat(tBingg.getText());
        ipa = Float.parseFloat(tIpa.getText());
        mtk = Float.parseFloat(tMtk.getText());
        nim = (bhs_ind + bhs_ingg + ipa + mtk)*10/100;
        
        tNim.setText(Float.toString(nim));
    }//GEN-LAST:event_bHitungActionPerformed

