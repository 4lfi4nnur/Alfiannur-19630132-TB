    public void tampil_cb(){
       try{
           String sql = "SELECT * FROM siswa";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {                
                cNik.addItem(rs.getString("nik"));
            }
            
            rs.last();
            int jumlahdata = rs.getRow();
            rs.first();
            
        } catch (SQLException e) {
       }
        
    }
    
    private void tampil_data(){
        try{
            String[]judul = {"NIK","Bahasa Indonesia","Bahasa Inggris","IPA","Matematika","NEM"};
            DefaultTableModel dtm = new DefaultTableModel(null,judul);
            tableNilai.setModel(dtm);
            String sql = "";
            if(tCari.getText().isEmpty()){
                sql= "select * from nilai";
            }else{
                sql = "select * from nilai where nik like '%"+tCari.getText()+"%'";
            }
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                String[]data={rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
                dtm.addRow(data);
            }
        }catch (SQLException ex){
            Logger.getLogger(FormPendaftaran.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
